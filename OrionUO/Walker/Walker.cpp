#include "Walker.h"
//----------------------------------------------------------------------------------
CWalker *g_Walker = NULL;
//----------------------------------------------------------------------------------
CWalker::CWalker()
{
	memset(m_Direction, 0, sizeof(m_Direction));
}
//---------------------------------------------------------------------------
void CWalker::IncSequence()
{
	if (m_Sequence == 255)
		m_Sequence = 1;
	else
		m_Sequence++;
}
//---------------------------------------------------------------------------
void CWalker::SetSequence(const uchar &seq, const uchar &dir)
{
	m_Sequence = seq;
	m_Direction[seq] = dir;
}
